int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	while (i <= 46340)
	{
		if (i * i == nb)
		{
			return (i);
		}
		else if (i * i > nb)
		{
			break ;
		}
		i++;
	}
	return (0);
}
